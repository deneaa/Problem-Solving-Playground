function sampleStats(count) {
    let totalSum = 0;
    let totalCount = 0;
    let mode = 0;
    let modeCount = 0;
    let sample = [];
    let minimum = Infinity;
    let maximum = -Infinity;
  
    for (let i = 0; i < count.length; i++) {
      if (count[i] !== 0) {
        for (let j = 0; j < count[i]; j++) {
          sample.push(i);
        }
        if (i < minimum) minimum = i;
        if (i > maximum) maximum = i;
        totalSum += i * count[i];
        totalCount += count[i];
  
        if (count[i] > modeCount) {
          modeCount = count[i];
          mode = i;
        }
      }
    }
  
    let mean = totalSum / totalCount;
  
    let median;
    let mid = Math.floor(totalCount / 2);
    if (totalCount % 2 === 1) {
      median = sample[mid];
    } else {
      median = (sample[mid - 1] + sample[mid]) / 2;
    }
    let result = [minimum, maximum, mean, median, mode];
  
    return result;
  }
  
